	                    <div class="answer" id="answer-1">
	                        <b>Gross yield:</b> A yield on investment property before any expenses. This is calculated as gross annual rent divided by purchase price.
                            <b>Cap rate:</b> Similar to gross yield but includes all of your expenses (except loan costs). This is calculated as net annual rent income in Year 1 divided by purchase price.
                            <b>Cash yield:</b> This is a measure of return on your initial investment after your first full year of ownership. This is calculated as net cash flow, after all expenses and loan payments, divided by initial investment amount.
                            <b>Annualized return:</b> Estimate of the value the investment generates over the time period for which you own it (including cash flow and appreciation).

